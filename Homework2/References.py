class Book():

    def __init__(self, key, author, title, publisher, date):
        self.key = key
        self.author = author
        self.title = title
        self.date = date
        self.publisher = publisher


class Journal():

    def __init__(self, key, author, title, journal, publisher, date, volume, number):
        self.key = key
        self.author = author
        self.title = title
        self.journal = journal
        self.publisher = publisher
        self.date = date
        self.volume = volume
        self.number = number


